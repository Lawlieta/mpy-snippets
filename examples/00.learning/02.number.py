# 数字
# Python3 支持 int、float、bool、complex（复数）
# 在 Python 3里，只有一种整数类型 int，表示为长整型
# 像大多数语言一样，数值类型的赋值和计算都是很直观的
# 内置的 type() 函数可以用来查询变量所指的对象类型

a, b, c, d = 20, 5.5, True, 4+3j
print(type(a), type(b), type(c), type(d))
